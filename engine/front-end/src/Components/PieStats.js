class PieStats extends Component {
    constructor(props) {
        super(props);
        registerTheme();
    }
    render() {
        let labels = Object.keys(this.props.data).sort((a, b) => this.props.data[b] - this.props.data[a]);
        let series = labels.map(l => {return {value: this.props.data[l], name: l};});
        let sum = 0;
        series.forEach(v => sum += v.value || 0);

        if (this.props.startFromPercent) {
            for (let k = 0; k < series.length; k++) {
                if (series[k].value / sum < this.props.startFromPercent / 100) {
                    let others = 0;
                    for (let i = k; i < series.length; i++) {
                        others += series[i].value;
                    }
                    labels.splice(k, labels.length - k);
                    series.splice(k, series.length - k);
                    labels.push(I18n.t('others'));
                    series.push({name: I18n.t('others'), value: others});
                    const d = {};
                    // eslint-disable-next-line
                    labels.forEach((l, i) => d[l] = series[i]);
                    labels = Object.keys(d).sort((a, b) => d[b].value - d[a].value);
                    series = labels.map(l => d[l]);
                    break;
                }
            }
        }

        const option = {
            tooltip : {
                trigger: 'item',
                formatter: "{a} <br/>{b} : {c} ({d}%)"
            },
            legend: {
                type: 'scroll',
                orient: 'vertical',
                right: 0,
                top: 0,
                bottom: 0,
                formatter: this.props.hideNumbersInLegend ? undefined : v => v + ' - ' + this.props.data[v],
                data: labels,
            },
            series : [
                {
                    name: this.props.series,
                    type: 'pie',
                    radius: this.props.radius || '65%',
                    center: ['45%', '55%'],
                    data: series,
                    itemStyle: {
                        emphasis: {
                            shadowBlur: 10,
                            shadowOffsetX: 0,
                            shadowColor: 'rgba(0, 0, 0, 0.5)'
                        }
                    }
                }
            ]
        };

        /*const ddd = labels.map((n, i) => {
            return {
                value: series[i],
                name: n,
                className: this.colors[i] || undefined,
                meta: 'Meta One'
            };
        });*/

        return (<ReactEcharts
            option={option}
            notMerge={true}
            style={{height: this.props.height || '350px', width: '100%'}}
            lazyUpdate={true}
            //               opts={{renderer: 'svg'}}
            theme={"westeros"}
        />);
    }
}

